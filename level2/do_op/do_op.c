#include "do_op.h"

int sums(int x, int y)
{
    printf("%d\n", (x + y));
}

int subs(int x, int y)
{
    printf("%d\n", (x - y));
}

int divs(int x, int y)
{    
    printf("%d\n", (x / y));
}

int crosss(int x, int y)
{
    printf("%d\n", (x * y));
}

int mods(int x, int y)
{
    printf("%d\n", (x % y));
}